package fa.training.assignment1;

public class LogicalExercise {
	public static void main(String[] args) {
		if(25!=39) {
			System.out.println("25! = 39");
		}
		if(25 <39) {
			System.out.println("25 < 39 ");
		}
		if(25 <= 39) {
			System.out.println("25 <= 39 ");
		}
		System.out.println("25 <= 39 ");
	}
}
